package ru.isbo1016.chumakov.prak6.pt1;

public class Student {
    public String name;
    public int IDnumber;

    public Student(String name, int IDnumber) {
        this.name = name;
        this.IDnumber = IDnumber;
    }

    public String getName() {
        return name;
    }

    public int getIDnumber() {
        return IDnumber;
    }

}
